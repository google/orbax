"""Generates V0 checkpoints for compatibility testing.

The checkpoints generated by this script are checked into the repository
statically. While our library's logic changes over time, user-held checkpoints
do not. Checking these in allows us to test the interaction between evolving
code and "frozen" static artifacts, ensuring long-term backward compatibility
that runtime generation (which only tests current library behavior) cannot
guarantee.
"""
import itertools
import json
import os
from typing import Any

from absl import app
from absl import flags
from etils import epath
import jax.numpy as jnp
from orbax.checkpoint import args
from orbax.checkpoint import test_utils
from orbax.checkpoint._src.checkpointers import checkpointer as v0_checkpointer
from orbax.checkpoint._src.checkpointers import standard_checkpointer
from orbax.checkpoint._src.handlers import composite_checkpoint_handler

FLAGS = flags.FLAGS


def _get_base_dir():
  if 'BUILD_WORKING_DIRECTORY' in os.environ:
    return os.path.join(
        os.environ['BUILD_WORKING_DIRECTORY'],
        'orbax/checkpoint/experimental/v1/_src/testing/compatibility/checkpoints',
    )
  return os.path.join(
      os.path.dirname(__file__),
      'checkpoints',
  )


_BASE_DIR = flags.DEFINE_string(
    'base_dir',
    _get_base_dir(),
    'Base directory to save checkpoints.',
)
_OVERWRITE = flags.DEFINE_bool(
    'overwrite',
    False,
    'Overwrite existing checkpoints.',
)


def create_pytree() -> dict[str, Any]:
  return {
      'a': jnp.array([0, 1, 2, 3, 4, 5, 6, 7], dtype=jnp.int32),
      'b': {'c': jnp.array([1, 2, 3], dtype=jnp.int32)},
  }


def create_json_object() -> dict[str, Any]:
  return {'metadata': 'json_data'}


def _standard_checkpointer_save_pytree(path: epath.Path) -> None:
  """Saves a direct checkpoint using StandardCheckpointer for pytree."""
  pytree = create_pytree()
  if _OVERWRITE.value:
    path.rmtree(missing_ok=True)
  with standard_checkpointer.StandardCheckpointer() as checkpointer:
    checkpointer.save(path, pytree)


def _checkpointer_save_composite_mixed(path: epath.Path) -> None:
  """Saves a composite checkpoint using Checkpointer + CompositeHandler."""
  json_object = create_json_object()
  pytree = create_pytree()
  checkpoint_args = args.Composite(**{
      'state': args.PyTreeSave(pytree),  # Represents pytree checkpointable.
      'metadata': args.JsonSave(json_object),
  })
  if _OVERWRITE.value:
    path.rmtree(missing_ok=True)
  with v0_checkpointer.Checkpointer(
      composite_checkpoint_handler.CompositeCheckpointHandler()
  ) as checkpointer:
    checkpointer.save(path, checkpoint_args)


def generate_v0_checkpoint(
    path: epath.Path,
    is_direct_checkpoint: bool = False,
) -> None:
  """Generates a V0 checkpoint with the given save function."""
  if is_direct_checkpoint:
    _standard_checkpointer_save_pytree(path)
    (path / 'descriptor').rmtree()  # GOOGLE_INTERNAL
  else:
    _checkpointer_save_composite_mixed(path)
    (path / 'state' / 'descriptor').rmtree()  # GOOGLE_INTERNAL


def delete_checkpoint_metadata_field(
    path: epath.Path, field_to_remove: str
) -> None:
  """Deletes a field from _CHECKPOINT_METADATA."""
  metadata_path = path / '_CHECKPOINT_METADATA'
  with open(metadata_path, 'r') as f:
    data = json.load(f)
  del data[field_to_remove]
  with open(metadata_path, 'w') as f:
    json.dump(data, f)


def v0_generate_case(
    is_direct_checkpoint: bool,
    has_checkpoint_metadata: bool,
    has_pytree_metadata: bool,
) -> None:
  """Generates a V0 checkpoint based on parameters."""
  ckpt_type = (
      'direct_checkpoint' if is_direct_checkpoint else 'composite_checkpoint'
  )
  metadata_dir = (
      'checkpoint_metadata_present'
      if has_checkpoint_metadata
      else 'checkpoint_metadata_missing'
  )
  pytree_dir = (
      'pytree_checkpointable_has_metadata'
      if has_pytree_metadata
      else 'pytree_checkpointable_missing_metadata'
  )
  path = (
      epath.Path(_BASE_DIR.value)
      / 'v0_checkpoints'
      / ckpt_type
      / metadata_dir
      / pytree_dir
  )

  generate_v0_checkpoint(path, is_direct_checkpoint=is_direct_checkpoint)

  if not has_checkpoint_metadata:
    metadata_path = path / '_CHECKPOINT_METADATA'
    if metadata_path.exists():
      metadata_path.unlink()

  if not has_pytree_metadata:
    pytree_metadata_path = path if is_direct_checkpoint else path / 'state'
    (pytree_metadata_path / '_METADATA').unlink()


def v0_missing_checkpoint_metadata_field(
    field_to_remove: str,
    is_direct_checkpoint: bool,
) -> None:
  """Saves V0 checkpoint and removes a field from _CHECKPOINT_METADATA."""
  ckpt_type = (
      'direct_checkpoint' if is_direct_checkpoint else 'composite_checkpoint'
  )
  path = (
      epath.Path(_BASE_DIR.value)
      / 'v0_checkpoints'
      / ckpt_type
      / 'non_critical_metadata_alterations'
      / f'missing_{field_to_remove}_metadata'
  )
  generate_v0_checkpoint(path, is_direct_checkpoint=is_direct_checkpoint)
  delete_checkpoint_metadata_field(path, field_to_remove)


def v0_missing_pytree_data(
    name: str,
    is_direct_checkpoint: bool,
    is_dir: bool = False,
    is_critical: bool = False,
) -> None:
  """Saves a checkpoint and removes data files or directories."""
  ckpt_type = (
      'direct_checkpoint' if is_direct_checkpoint else 'composite_checkpoint'
  )
  prefix = 'dir' if is_dir else 'file'
  alteration_type = (
      'critical_metadata_alterations'
      if is_critical
      else 'non_critical_metadata_alterations'
  )
  path = (
      epath.Path(_BASE_DIR.value)
      / 'v0_checkpoints'
      / ckpt_type
      / alteration_type
      / f'missing_pytree_data_{prefix}_{name}'
  )
  generate_v0_checkpoint(path, is_direct_checkpoint=is_direct_checkpoint)

  target_path = path if is_direct_checkpoint else path / 'state'
  if is_dir:
    (target_path / name).rmtree()
  else:
    (target_path / name).unlink()


def main(argv):
  del argv
  epath.Path(_BASE_DIR.value).mkdir(parents=True, exist_ok=True)

  test_utils.set_tensorstore_driver_for_test()

  print('Generating V0 Checkpoints...')
  # Three categories we'll generate against:
  # 1. Is Checkpoint Metadata present?
  # - Yes, No
  # 2. Item_handler type (save method which dictates item_handler contents)
  # - dict(composite), str(direct_pytree checkpoint)
  # Note we will treat v0 checkpoint handlers as unregistered by default when
  # we test loading.
  # 3. Is Pytree? (Pytree Metadata present in checkpointable dir?)
  # - Yes, No

  # The directory structure will follow the categories above:
  # base_dir /
  # 'v0_checkpoints' /
  # <'direct_checkpoint' | 'composite_checkpoint'> /
  # <checkpoint_metadata_present|missing> /
  # <pytree_checkpointable_has|missing>_metadata /

  for is_dir_ckpt, has_metadata, has_pytree in itertools.product(
      [True, False], repeat=3
  ):
    v0_generate_case(
        is_direct_checkpoint=is_dir_ckpt,
        has_checkpoint_metadata=has_metadata,
        has_pytree_metadata=has_pytree,
    )

  # --- ADDITIONAL CORRUPTIONS ---
  # The directory structure will be as follows:
  # base_dir /
  # 'v0_checkpoints' /
  # <'composite_checkpoint'|'direct_checkpoint'> /
  # <'critical_metadata_alterations'/'critical_pytree_data_alterations'> /
  # <field_to_remove> /

  # --- NON-CRITICAL METADATA ALTERATIONS ---
  # These metadata fields are safely considered non-critical for backward
  # compatibility.
  fields_to_remove = [
      'item_handlers',
      'metrics',
      'performance_metrics',
      'init_timestamp_nsecs',
      'commit_timestamp_nsecs',
      'custom_metadata',
  ]
  for field, is_dir_ckpt in itertools.product(fields_to_remove, [True, False]):
    v0_missing_checkpoint_metadata_field(field, is_dir_ckpt)

  # --- NON-CRITICAL PYTREE DATA ALTERATIONS ---
  # These files/dirs are non-critical to loading the pytree. Note: absence of
  # _sharding will only give an error if abstract pytree is not provided.
  non_critical_tasks = [
      ('_sharding', False),
      ('array_metadatas', True),
  ]
  for name, is_directory in non_critical_tasks:
    for is_dir_ckpt in [True, False]:
      v0_missing_pytree_data(
          name, is_dir_ckpt, is_dir=is_directory, is_critical=False
      )

  # --- CRITICAL PYTREE DATA ALTERATIONS ---
  # These files/dirs are critical for loading the pytree successfully.
  critical_tasks = [
      ('manifest.ocdbt', False),
      ('d', True),
  ]
  for name, is_directory in critical_tasks:
    for is_dir_ckpt in [True, False]:
      v0_missing_pytree_data(
          name, is_dir_ckpt, is_dir=is_directory, is_critical=True
      )

  print(f'V0 Checkpoints generated at {_BASE_DIR.value}')


if __name__ == '__main__':
  app.run(main)
