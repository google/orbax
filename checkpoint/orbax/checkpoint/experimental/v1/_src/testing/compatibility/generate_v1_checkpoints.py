"""Generates V1 checkpoints for compatibility testing.

The checkpoints generated by this script are checked into the repository
statically. While our library's logic changes over time, user-held checkpoints
do not. Checking these in allows us to test the interaction between evolving
code and "frozen" static artifacts, ensuring long-term backward compatibility
that runtime generation (which only tests current library behavior) cannot
guarantee.
"""
import itertools
import json
import os
from typing import Any

from absl import app
from absl import flags
from etils import epath
import jax.numpy as jnp
from orbax.checkpoint import test_utils
import orbax.checkpoint.experimental.v1 as ocp
from orbax.checkpoint.experimental.v1._src.handlers import registration


FLAGS = flags.FLAGS


def _get_base_dir():
  if 'BUILD_WORKING_DIRECTORY' in os.environ:
    return os.path.join(
        os.environ['BUILD_WORKING_DIRECTORY'],
        'orbax/checkpoint/experimental/v1/_src/testing/compatibility/checkpoints',
    )
  return os.path.join(
      os.path.dirname(__file__),
      'checkpoints',
  )


_BASE_DIR = flags.DEFINE_string(
    'base_dir',
    _get_base_dir(),
    'Base directory to save checkpoints.',
)
_OVERWRITE = flags.DEFINE_bool(
    'overwrite',
    False,
    'Overwrite existing checkpoints.',
)


def create_pytree() -> dict[str, Any]:
  return {
      'a': jnp.array([0, 1, 2, 3, 4, 5, 6, 7], dtype=jnp.int32),
      'b': {'c': jnp.array([1, 2, 3], dtype=jnp.int32)},
  }


def create_json_object() -> dict[str, Any]:
  return {'metadata': 'json_data'}


def generate_v1_checkpoint(path: epath.Path) -> None:
  """Saves a V1 composite checkpoint using save_checkpointables."""
  checkpointables = {
      'state': create_pytree(),
      'metadata': create_json_object(),
  }
  if _OVERWRITE.value:
    path.rmtree(missing_ok=True)

  registry = registration.local_registry()
  registry.add(ocp.handlers.PyTreeHandler, checkpointable_name='state')
  registry.add(ocp.handlers.JsonHandler, checkpointable_name='metadata')
  with ocp.Context(
      checkpointables_options=ocp.options.CheckpointablesOptions(
          registry=registry
      )
  ):
    ocp.save_checkpointables(path, checkpointables)
    (path / 'descriptor').rmtree()  # GOOGLE_INTERNAL


def delete_checkpoint_metadata_field(
    path: epath.Path, field_to_remove: str
) -> None:
  """Deletes a field from _CHECKPOINT_METADATA."""
  metadata_path = path / '_CHECKPOINT_METADATA'
  with open(metadata_path, 'r') as f:
    data = json.load(f)
  del data[field_to_remove]
  with open(metadata_path, 'w') as f:
    json.dump(data, f)


def v1_generate_case(
    has_checkpoint_metadata: bool,
    has_pytree_metadata: bool,
) -> None:
  """Generates a V1 checkpoint based on parameters."""
  metadata_dir = (
      'checkpoint_metadata_present'
      if has_checkpoint_metadata
      else 'checkpoint_metadata_missing'
  )
  pytree_dir = (
      'pytree_checkpointable_has_metadata'
      if has_pytree_metadata
      else 'pytree_checkpointable_missing_metadata'
  )
  path = (
      epath.Path(_BASE_DIR.value)
      / 'v1_checkpoints'
      / 'composite_checkpoint'
      / metadata_dir
      / pytree_dir
  )

  generate_v1_checkpoint(path)

  if not has_checkpoint_metadata:
    metadata_path = path / '_CHECKPOINT_METADATA'
    if metadata_path.exists():
      metadata_path.unlink()

  if not has_pytree_metadata:
    pytree_metadata_path = path / 'state'
    (pytree_metadata_path / '_METADATA').unlink()


def v1_missing_checkpoint_metadata_field(
    field_to_remove: str,
) -> None:
  """Saves V1 checkpoint and removes a field from _CHECKPOINT_METADATA."""
  path = (
      epath.Path(_BASE_DIR.value)
      / 'v1_checkpoints'
      / 'composite_checkpoint'
      / 'non_critical_metadata_alterations'
      / f'missing_{field_to_remove}_metadata'
  )
  generate_v1_checkpoint(path)
  delete_checkpoint_metadata_field(path, field_to_remove)


def v1_missing_pytree_data(
    name: str, is_dir: bool = False, is_critical: bool = False
) -> None:
  """Saves a checkpoint and removes data files or directories."""
  prefix = 'dir' if is_dir else 'file'
  alteration_type = (
      'critical_metadata_alterations'
      if is_critical
      else 'non_critical_metadata_alterations'
  )
  path = (
      epath.Path(_BASE_DIR.value)
      / 'v1_checkpoints'
      / 'composite_checkpoint'
      / alteration_type
      / f'missing_pytree_data_{prefix}_{name}'
  )
  generate_v1_checkpoint(path)

  target_path = path / 'state'
  if is_dir:
    (target_path / name).rmtree()
  else:
    (target_path / name).unlink()


def v1_dummy_checkpointable_present() -> None:
  """Saves a checkpointables checkpoint and adds a dummy checkpointable."""
  # Note: this is a critical alteration because a checkpointable is added to
  # the checkpoint and causes error when calling load_checkpointables with
  # `abstract_checkpointables=None`, which attempts to load all contents of the
  # checkpoint and will fail to restore the contents of a dummmy checkpointable.
  path = (
      epath.Path(_BASE_DIR.value)
      / 'v1_checkpoints'
      / 'composite_checkpoint'
      / 'critical_general_alterations'
      / 'dummy_checkpointable_added'
  )
  generate_v1_checkpoint(path)
  (path / 'dummy').mkdir()
  (path / 'dummy' / '_METADATA').write_text('dummy')


def v1_delete_checkpointable() -> None:
  """Saves a checkpointables checkpoint and deletes a checkpointable."""
  # Note: this is a non-critical alteration because each checkpointable is
  # separable and we allow deleting specific checkpointables as needed.
  # Restoring the checkpoint does not raise an error, as long as we don't
  # attempt to restore the deleted checkpointable.
  path = (
      epath.Path(_BASE_DIR.value)
      / 'v1_checkpoints'
      / 'composite_checkpoint'
      / 'non_critical_general_alterations'
      / 'deleted_checkpointable'
  )
  generate_v1_checkpoint(path)
  (path / 'state').rmtree()


def main(argv):
  del argv
  epath.Path(_BASE_DIR.value).mkdir(parents=True, exist_ok=True)
  test_utils.set_tensorstore_driver_for_test()
  print('Generating V1 Checkpoints...')

  # The directory structure will follow the categories above:
  # base_dir /
  # 'v1_checkpoints' /
  # <'composite_checkpoint'> /
  # <checkpoint_metadata_present|missing> /
  # <pytree_checkpointable_has|missing>_metadata /
  # Note we will be testing against unregistered handlers in our testing
  # files.
  for has_metadata, has_pytree in itertools.product(
      [True, False], repeat=2
  ):
    v1_generate_case(
        has_checkpoint_metadata=has_metadata,
        has_pytree_metadata=has_pytree,
    )

  # --- ADDITIONAL CORRUPTIONS ---
  # The directory structure will be as follows:
  # base_dir /
  # 'v1_checkpoints' /
  # 'composite_checkpoint' /
  # <Alteration_Type> /
  # <Specific_Alteration> /

  # --- NON-CRITICAL METADATA ALTERATIONS ---
  fields_to_remove = [
      'item_handlers',
      'metrics',
      'performance_metrics',
      'init_timestamp_nsecs',
      'commit_timestamp_nsecs',
      'custom_metadata',
  ]
  for field in fields_to_remove:
    v1_missing_checkpoint_metadata_field(field)

  # --- NON-CRITICAL PYTREE DATA ALTERATIONS ---
  # These files/dirs are non-critical to loading the pytree. Note: absence of
  # _sharding will only give an error if abstract pytree is not provided.
  non_critical_tasks = [
      ('_sharding', False),
      ('array_metadatas', True),
  ]
  for name, is_directory in non_critical_tasks:
    v1_missing_pytree_data(name, is_dir=is_directory, is_critical=False)

  # --- CRITICAL PYTREE DATA ALTERATIONS ---
  critical_tasks = [
      ('manifest.ocdbt', False),
      ('d', True),
  ]
  for name, is_directory in critical_tasks:
    v1_missing_pytree_data(name, is_dir=is_directory, is_critical=True)

  # --- GENERAL ALTERATIONS ---
  v1_dummy_checkpointable_present()
  v1_delete_checkpointable()

  print(f'V1 Checkpoints generated at {_BASE_DIR.value}')


if __name__ == '__main__':
  app.run(main)
